    {
        $videos = Video::where('status', 'public')->inRandomOrder()->limit(20)->get();
        return view('welcome', ['videos' => $videos]);
    }

    // Search Page
    public function search(Request $request)
    {
        if ($request->has('search')) {
            $query = $request->get('search');
            if ($query) {
                $channels = Channel::where('name', 'like', '%' . $query . '%')->get();
                $videos = Video::where('title', 'like', '%' . $query . '%')->where('status', 'public')->get();
                return view('search', ['channels' => $channels, 'videos' => $videos, 'query' => $query]);
            } else {
                return back()->with('error', 'Search field should not be empty.');
            }
        } else {
            return redirect()->route('home.index');
        }
    }

    public function trending()
    {
        $videos = Video::where('status', 'public')->inRandomOrder()->limit(10)->get();
        return view('trending', ['videos' => $videos]);
    }

    public function subscriptions()
    {
        $channels = Auth::user()->manyChannels()->inRandomOrder()->get();
        return view('subscriptions', ['channels' => $channels]);
    }

    public function history()
    {
        $videos = Auth::user()->manyVideos()->orderBy('user_video.created_at', 'desc')->paginate(10);
        return view('history', ['videos' => $videos]);
    }











